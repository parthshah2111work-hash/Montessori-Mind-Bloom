export function getPhaseForAge(ageMonths: number): ActivityPhase {
  if (ageMonths < 4)  return "newborn";
  if (ageMonths < 8)  return "discoverer";
  if (ageMonths < 12) return "mover";
  if (ageMonths < 16) return "firstSteps";
  if (ageMonths < 20) return "explorer";
  if (ageMonths < 24) return "communicator";
  if (ageMonths < 28) return "builder";
  if (ageMonths < 36) return "storyteller";
  if (ageMonths < 48) return "questioner";
  return "preSchooler";
}

